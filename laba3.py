#Натуральные числа, содержащие как минимум один ноль. Выводить прописью количество нулей в каждом числе.

with open('laba3.txt', 'r') as file:
    while True:  # Бесконечный цикл для построчного чтения файла
        line = file.readline()  # Считываем строку из файла
        if not line:
            break

        numbers = line.split()


        for number in numbers:
            if number.isdigit():  # Проверяем, что это действительно число
                zero_count = number.count('0')  # Считаем, сколько нулей в числе

                if zero_count > 0:

                    num_to_word = {
                        '0': 'ноль',
                        '1': 'один',
                        '2': 'два',
                        '3': 'три',
                        '4': 'четыре',
                        '5': 'пять',
                        '6': 'шесть',
                        '7': 'семь',
                        '8': 'восемь',
                        '9': 'девять'
                    }

                    # Преобразуем количество нулей в число прописью
                    zero_count_word = ''.join([num_to_word[digit] for digit in str(zero_count)])

                    # Выводим результат
                    print(f"Число: {number} содержит {zero_count_word} нулей.")
                else:
                    # Если нулей в числе нет, можно ничего не выводить
                    print(f"Число: {number} не содержит нулей.")
            else:
                # Если это не число, просто сообщаем об этом
                print(f"'{number}' не является натуральным числом.")
